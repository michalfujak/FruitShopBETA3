package com.shop.fruit.fruitshopbeta3.Modul;

public class Banner {
    /**
     * @variables
     */
    private String id;
    private String name_;
    private String link;
    private String values_special;
    private String comment;

    public Banner() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName_() {
        return name_;
    }

    public void setName_(String name_) {
        this.name_ = name_;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getValues_special() {
        return values_special;
    }

    public void setValues_special(String values_special) {
        this.values_special = values_special;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }
}
